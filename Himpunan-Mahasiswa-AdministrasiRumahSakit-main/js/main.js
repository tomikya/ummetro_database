// banner
function resize() {
    var konten = document.getElementsByClassName("content-awal");
    for (i = 0; i < konten.length; i++) {
        konten[i].style.width = window.innerWidth + 'px';
    }
}
window.addEventListener('resize', function () {
    resize();
});

resize();

